public class VetorExemplo {
    public static void main(String[] args) {
        // Declara e inicializa o vetor com 5 valores
        int[] vetor = {10, 20, 30, 40, 50};

        // Acessa e exibe o terceiro elemento do vetor
        // Lembre-se que a indexação é baseada em zero, então o terceiro elemento está no índice 2
        System.out.println("O terceiro elemento do vetor é: " + vetor[2]);
    }
}
